//Parametros y argumentos en Funciones

function sumar(a , b){ // a y b son parametros de una funcion
    console.log( a + b);
}
sumar(2,4); // 2 y 4 son argumentos son valores reales.

sumar(15, 67)

function saludar (nombre, apellido){
    console.log(`Hola ${nombre} ${apellido}`);
}

saludar('Erick', 'Fuentes');
saludar('Erick');
